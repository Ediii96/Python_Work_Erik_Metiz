# 7-4. Дополнения для пиццы: напишите цикл, который предлагает пользователю вводить дополнения для пиццы до тех пор,
# пока не будет введено значение 'quit’. При вводе каждого дополнения выведите сообщение о том,
# что это дополнение включено в заказ.

def value_pizza():
    menu_pizza = ['гавайская', 'сырная', 'мясная', 'ананасовая', 'чили', 'маргагита']
    print('Сделайте ваш заказ\n')

    def output(num_pizza='x'):
        n = 0
        for menu in menu_pizza:
            n += 1
            if num_pizza == 'x':
                print(str(n) + '. ' + menu[0].upper() + menu[1:])
            elif int(num_pizza) == 0:
                break
            elif int(num_pizza) == menu_pizza.index(menu) + 1:
                print('Вы выбрали пиццу: ' + menu_pizza[num_pizza - 1].title())

    output()
    print('0. Выход')
    num_pizza = input('Введите номер пиццы которую хотите заказть: \n')
    output(int(num_pizza))

    true_topping = input('Если нужны добавки в пиццу, нажмите Y, если нет то N\n')
    if true_topping.lower() == 'y':
        prompt = 'Введите добавки в пиццу: \n'
        prompt += '(Для выхода нажмите Q)\n'
        topping_list = []
        while True:
            topping = input(prompt)
            topping_list.append(str(topping))
            if str(topping).lower() == 'q':
                del topping_list[-1]
                print('Ваша пицца готовится с добавками: ')
                for t in topping_list:
                    print(t)
                print('Ожидайте...')
                break
    else:
        print('Ваша пицца готовиться. Ожидайте...')


