# 7-5. Билеты в кино: кинотеатр установил несколько вариантов цены на билеты в зависимости от возраста посетителя.
# Для посетителей младше 3 лет билет бесплатный; в возрасте от 3 до 12 билет стоит $10; наконец,
# если возраст посетителя больше 12, билет стоит $15. Напишите цикл, который предлагает пользователю ввести возраст
# и выводит цену билета.

def movie_ticket():
    while True:
        print("(Для выхода введите: '0')")
        age = int(input('Введите ваш возраст: '))
        if age == 0:
            break
        elif age < 3:
            print('Для вас билет бесплатный\n')
        elif 3 <= age <= 12:
            print('Стоимость билета $10\n')
        elif age > 12:
            print('Стоимость билета $15\n')
    print('Выполнен выход из программы\n')


